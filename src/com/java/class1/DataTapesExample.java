package com.java.class1;

public class DataTapesExample {
    public static void main(String[] args) {
        //declare and initialize a variable integer
        int num1 = 10;
        int num2 = 8;
        int result = num1 + num2;

        System.out.println(num2 % num1);   //8   8 % 10

        System.out.println(num1 % num2);   //2   10 % 8

        System.out.println(num1 / num2);   //1   10 / 8

        System.out.println(num2 / num1);   //0   8 / 10

        System.out.println(num2 * num1);   //80

        System.out.println(result);        //18

        System.out.println("result");      //result
    }
}
